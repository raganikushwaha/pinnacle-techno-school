                    <h2>Eligibility Criteria for ntse :</h2>
                    <p>
                        Candidates currently studying in Std. 10th can apply for the NTSE. The stage 1 of the NTSE examination is conducted by different states/UT,
                        thus depending upon the location of your school, you can apply for the examination. There is no domicile restriction for the examination. 
                        Candidates registered under Open Distance Learning (ODL) are also eligible for this scholarship, provided he/she is less than 18 years of age, unemployed and appearing for the Std.
                        10th examination for the first time.
                    </p>
                    
                    <h2>Selection Process :</h2>
                    <p>
                    The selection process of NTSE has two stages viz. Stage 1 and 2.
                    Stage 1 is conducted by the State/ UT and stage 2 is conducted by the NCERT. 
                    Only the students qualifying stage 1 are eligible for stage 2 and the students qualifying stage 2 would be awarded the scholarship.
                    </p>
                    
                    <h2>Number of Scholarships awarded :</h2>
                    <h3>Every year 1000 Scholarships are awarded, the amount of the scholarship is as follows :</h3>
                    <ul>
                        <li>Scholarship of Rs. 1250 per month for 11th and 12th Std.</li>
                        <li>Scholarship of Rs. 2000 per month for UG and PG.</li>
                        <li>Scholarship for Ph.D. will be fixed as per the UGC norms.</li>
                    </ul>
                    
