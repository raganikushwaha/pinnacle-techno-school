                    <p class="faq-question">Q. For which class NTSE is conducted?</p>
                    <p class="faq-answer">A. NTSE examination is conducted only for Class 10 students.</p>
                    
                    <p class="faq-question">Q. What type of questions are asked in NTSE?</p>
                    <p class="faq-answer">A. NTSE examination is one of the most prestigious scholarship tests in India. Multiple Choice Questions (MCQs) are asked in the NTSE exam.</p>
                    
                    <p class="faq-question">Q. Which state has the toughest NTSE paper?</p>
                    <p class="faq-answer">A. Delhi(UT), West Bengal and Andhra Pradesh states are known to set the toughest NTSE papers.</p>
                    
                    <p class="faq-question">Q. What is the success formula to crack NTSE?</p>
                    <p class="faq-answer">A.
    NTSE exam is meticulously designed by subject experts to check the basic conceptual knowledge of the student in each subject. So, to crack the NTSE exam, very firstly, student needs to be very thorough in studying text books (NCERT) and practice the topic-wise NTSE mock tests designed by Pinnacle. Time bound practice & accuracy are the essentials. The overall analysis report of each test will give you in-sight on the areas in which you need to improve
    </p>
                    
