                    <p>
                       National Talent Search Examination (NTSE) is one of the most prestigious national level scholarship programs. 
                       Its aim is to identify talented individuals and nurture them. This exam is conducted by National Council of Education Research and Training (NCERT) for Std.
                       10th students. The exam is held on two stages viz. 
                       Stage 1 (State/UT level) and Stage 2 (National level). Every year about 50 Lakh candidates apply for this program and roughly 1000 candidates are awarded this scholarship. 
                       This article will provide you with all the details regarding NTSE
                    </p>
                        
